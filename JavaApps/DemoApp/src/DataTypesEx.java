
public class DataTypesEx {

	public static void main(String[] args) 
	{
			int x = 100;
			System.out.println("X value is " + x);
			
			float y = 12.45f;
			System.out.println("Y value is " + y);
			
			char ch = '%';
			System.out.println("Ch value is " + ch);
			
			boolean b = true;
			System.out.println("Boolean Value is " + b);
	}
}
